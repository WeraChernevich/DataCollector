package org.example.metroMain;

public class MetroStationDepth {
    private String name;
    private String depth;

    public MetroStationDepth(String name, String depth) {
        this.name = name;
        this.depth = depth;
    }

    public String getName() {
        return name;
    }

    public String getDepth() {
        return depth;
    }
}
